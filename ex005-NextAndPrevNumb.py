n1 = float(input("Digite um número: ")) #Recolhe número do usuário
n2 = n1 + 1 #Adiciona 1
n3 = n1 - 1 #Subtraí 1
print("O número sucessor de {} é {}!\nO número antecessor de {} é {:.1f}!".format(n1, n2, n1, n3)) #Mostra os resultados
#Declarei a variável n1 como FLOAT porque se o fosse INT e alguém digitarsse número quebrado daria erro
